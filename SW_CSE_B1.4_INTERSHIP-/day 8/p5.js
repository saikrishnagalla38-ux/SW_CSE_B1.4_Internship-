 // Primitive Data Types
// 1. String: used for text
// 2. Number: integers / decimal
// 3. Bigint: very large integers
// 4. Boolean: true/false
// 5. undefined: variable is declared but not assigned
// 6. Null: Represents an intentional empty value
// 7. Symbol: to create unique identifiers

// Non-Primitive Data types:
// Are more complex and are stored by reference
// 1. Object: to store collections of key-value pairs
// 2. Array: Special type of object for ordered lists
// 3. Function: are also considered as objects